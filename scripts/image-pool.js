// Track used indices per category for variety
const usedIndices = {};

export function getNextImage(category) {
  const images = CATEGORY_IMAGES[category] || CATEGORY_IMAGES.people;
  if (!usedIndices[category]) usedIndices[category] = 0;
  const index = usedIndices[category];
  usedIndices[category] = (index + 1) % images.length;
  return images[index];
}

export function resetImageIndices() {
  Object.keys(usedIndices).forEach(k => usedIndices[k] = 0);
}

export function getRandomImage(category) {
  const images = CATEGORY_IMAGES[category] || CATEGORY_IMAGES.people;
  return images[Math.floor(Math.random() * images.length)];
}
